  <h2>Is this secure?</h2>

  <p>You do know this is a joke right?</p>

  <p>For other more secure means of communication I suggest 
  you look into <a href="http://www.openpgp.org/about_openpgp/" target="_blank">OpenPGP standard</a>, a non-proprietary protocol 
  for encrypting email using public key cryptography.</p>

  <p>Take a look also at <a href="http://www.gnupg.org/" target="_blank">GNU Privacy Guard (GPG)</a> a complete 
  and free implementation of the OpenPGP standard.</p>

