  <h2>What is this?</h2>
  <p>It is well known that the NSA collects all digital information. 
  Recent events regarding leaked presentation slides had made this event 
  apparent. One way to protect yourself against this is to use 
  cryptography to encrypt your communication.</p>

  <p>However, using encrypted communication is just telling the NSA that 
  you have something to hide. In fact the NSA  proactively stores all 
  encrypted messages so that they can take their time to break the 
  encryption.</p>

  <p>Hipster Crypto will disguise your encrypted message with words 
  commonly used by your average 'hipster'. The goal is to trick the NSA 
  into thinking that you are just two hipster talking with each other.</p>

