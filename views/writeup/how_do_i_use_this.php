  <h2>How do I use this?</h2>
  
  <p>Type in your secret message.</p>
  
  <p>Choose a secret passphrase that you and the person that is 
  going to receive you message knows. Figure out a secure way 
  of exchanging passphrase.</p>
  
  <p>Click encrypt, copy paste the encrypted message to your 
  instant message or email.</p>
  
  <p>To decrypt simple copy and paste the encrypted message, key 
  in the secret passphrase and click decrypt.</p>
